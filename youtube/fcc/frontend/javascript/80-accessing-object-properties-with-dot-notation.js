// ACCESSING PROPERTIES ON AN OBJECT
//
// DOT NOTATION
// Using . after the variable name and the name, followed by the name of
// properties You want to access VARNAME.PROPERTIES

var testObj = {
  "hat": "ballcap",
  "shirt": "jersey",
  "shoes": "cleats",
};

var hatValue = testObj.hat;
var shirtValue = testObj.shirt;

console.log(hatValue); //ballcap
console.log(shirtValue); //jersey
