/* use single quotes '' to surround double quotes "" */
var myStr = '<a href="https://WWW.foo.com" target=_blank>Link</a>';

console.log(myStr);

// or You can use backtics to surround double and single quotes
var myStr = `'<a href="https://WWW.foo.com" target=_blank>Link</a>'`;

console.log(myStr);
