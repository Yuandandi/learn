// FOR LOOP
// initialization, condition, final expression

var ourArray = [];

// initializing i equals to 0 and while 1 is less than 5 will continue to run through the loop over and over until false(which < 5), the end of each iteration will increment by 1 (i++)
for (var i = 0; i < 5; i++) {
  ourArray.push(i);
}
